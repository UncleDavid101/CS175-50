num1 = int(input("Enter a number: "))
num2 = int(input("Enter another number: "))
print(f'The sum of the two numbers is {num1 + num2}')
print(f'The difference of the two numbers is {num1 - num2}')
print(f'The product of the two numbers is {num1 * num2}')
print(f'The average of the two numbers is {(num1 + num2) / 2 }')
print(f'The distance of the two numbers is {abs(num1 - num2)}')
print(f'The maximum of the two numbers is {max(num1, num2)}')
print(f'The minimum of the two numbers is {min(num1, num2)}')
